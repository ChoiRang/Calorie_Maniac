class FoodHistory:
	def __init__(self, food_history_index=0, food_index=0, food_name=None, food_kcal=0, food_date=None, food_image=None, food_month=0, food_week=0, food_day = 0):
		self.food_history_index = food_history_index
		self.food_index = food_index
		self.food_name = food_name
		self.food_kcal = food_kcal
		self.food_date = food_date
		self.food_image = food_image
		self.food_month = food_month
		self.food_week = food_week
		self.food_day = food_day



